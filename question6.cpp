#include <iostream>
using namespace std;

int main() {
    double num1, num2, sum;

    // Input first number
    cout << "Enter the first number: ";
    cin >> num1;

    // Input second number
    cout << "Enter the second number: ";
    cin >> num2;

    // Calculate the sum
    sum = num1 + num2;

    // Display the result
    cout << "Sum: " << sum << std::endl;

    return 0;
}
